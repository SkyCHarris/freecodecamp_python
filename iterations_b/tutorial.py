#Definite Loops

# -We have a list of items- a finite set of things
# -We can write a loop to run the loop once for each of the items in a set using 'for' construct
# -These loops are called "definite loops" because they execute an exact no. of times
# -"Definite loops iterate through the members of a set"

# A Simple Definite Loop

for i in [5, 4, 3, 2, 1] :
    print(i)
print('Blastoff!')

# A Definite Loop with Strings

friends = ['Joseph', 'Glenn', 'Sally']
for friend in friends :
    print('Happy new Year:', friend)
print('Done!')

